package Entities;
public class Genre {
    private  int id;
    private String genre_name;
    public int getId()
    {
        return id;
    }

    public void setId (int id)
    {
        this.id = id;
    }

    public String getGenre_name()
    {
        return genre_name;
    }

    public void setGenre_name(String name)
    {
        this.genre_name = name;
    }


}

